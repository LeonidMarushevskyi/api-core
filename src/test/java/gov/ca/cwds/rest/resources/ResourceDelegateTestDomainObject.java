package gov.ca.cwds.rest.resources;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.annotation.JsonProperty;

import gov.ca.cwds.rest.api.Request;
import gov.ca.cwds.rest.api.Response;
import gov.ca.cwds.rest.api.domain.DomainObject;

import java.util.Set;

public class ResourceDelegateTestDomainObject extends DomainObject implements Request, Response {
  @JsonProperty("id")
  private Long id;
  private Set messages;

  public ResourceDelegateTestDomainObject() {
    super();
  }

  @JsonCreator
  public ResourceDelegateTestDomainObject(Long id) {
    super();
    this.id = id;
  }

  public Long getId() {
    return id;
  }

  /*
   * (non-Javadoc)
   * 
   * @see java.lang.Object#hashCode()
   */
  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((id == null) ? 0 : id.hashCode());
    return result;
  }

  /*
   * (non-Javadoc)
   * 
   * @see java.lang.Object#equals(java.lang.Object)
   */
  @Override
  public boolean equals(Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    ResourceDelegateTestDomainObject other = (ResourceDelegateTestDomainObject) obj;
    if (id == null) {
      if (other.id != null)
        return false;
    } else if (!id.equals(other.id))
      return false;
    return true;
  }

  @Override
  public boolean hasMessages() {
    return messages != null && !messages.isEmpty();
  }
  public void setMessages(Set messages){
    this.messages = messages;
  }

  @Override
  public Set getMessages() {
    return messages;
  }
}
